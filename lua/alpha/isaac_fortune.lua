--- @param fortunes table<number, table<number, string>>
--- @return table<number, string>
local function get_fortune(fortunes)
    -- selects an entry from fortunes randomly
    math.randomseed(os.time())
    local ind = math.random(1, #fortunes)
    return fortunes[ind]
end

--- @return table<number, string>
--- @param opts number|table? optional
local main = function(opts)
    local options = {
        max_width = 70,
        fortune_list = fortune_list,
    }

    if type(opts) == "number" then
        options.max_width = opts
    elseif type(opts) == "table" then
        options = vim.tbl_extend("force", options, opts)
    end

    local fortune = get_fortune(options.fortune_list)

    return fortune
end

return main
